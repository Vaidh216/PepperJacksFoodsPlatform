package com.pepperjacks.foodplatform.util;

import org.springframework.stereotype.Component;

@Component
public class DistanceCalculator {
    
    private static final double EARTH_RADIUS_KM = 6371.0;
    
    /**
     * Calculate distance between two points using Haversine formula
     * @param lat1 Latitude of point 1
     * @param lon1 Longitude of point 1
     * @param lat2 Latitude of point 2
     * @param lon2 Longitude of point 2
     * @return Distance in kilometers
     */
    public double calculateDistance(double lat1, double lon1, double lat2, double lon2) {
        double dLat = Math.toRadians(lat2 - lat1);
        double dLon = Math.toRadians(lon2 - lon1);
        
        double a = Math.sin(dLat / 2) * Math.sin(dLat / 2)
                + Math.cos(Math.toRadians(lat1)) * Math.cos(Math.toRadians(lat2))
                * Math.sin(dLon / 2) * Math.sin(dLon / 2);
        
        double c = 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1 - a));
        
        return EARTH_RADIUS_KM * c;
    }
    
    /**
     * Calculate estimated delivery time based on distance
     * Assumes average speed of 20 km/h
     */
    public int calculateEstimatedDeliveryMinutes(double distanceKm) {
        double averageSpeedKmPerHour = 20.0;
        int travelTimeMinutes = (int) Math.ceil((distanceKm / averageSpeedKmPerHour) * 60);
        int prepTimeMinutes = 25; // Kitchen preparation time
        return prepTimeMinutes + travelTimeMinutes;
    }
}

